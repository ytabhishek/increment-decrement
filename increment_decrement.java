package Basics;

public class increment_decrement {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x++; //increment operator
        System.out.println(x);
        x--;  //decrement operator
        System.out.println(x);

        // x++ matlb pahle print karo fir add
        // ++x matlb pahle add karo fir print
        // x-- matlb pahle print karo fir subtract
        // --x matlb pahle subtract karo fir print
    }
}
